/**
 * FileUpload
 */
public class FileUpload {

    String name;
    Byte[] bytes;

    public FileUpload(){

    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public Byte[] getBytes() {
        return bytes;
    }
    public void setBytes(Byte[] bytes) {
        this.bytes = bytes;
    }
}